/*
 * An array is a collection of items stored at contiguous memory locations. The
 * idea is to store multiple items of the same type together. This makes it
 * easier to calculate the position of each element by simply adding an offset
 * to a base value, i.e., the memory location of the first element of the array
 * (generally denoted by the name of the array).
 * 
 * 
 * Following are the basic operations supported by an array.
 * 
 * Traverse − print all the array elements one by one.
 * 
 * Insertion − Adds an element at the given index.
 * 
 * Deletion − Deletes an element at the given index.
 * 
 * Search − Searches an element using the given index or by the value.
 * 
 * Update − Updates an element at the given index.
 * 
 * 
 * 
 * Need of using Array
 * In computer programming, the most of the cases requires to store the large
 * number of data of similar type. To store such amount of data, we need to
 * define a large number of variables. It would be very difficult to remember
 * names of all the variables while writing the programs. Instead of naming all
 * the variables with a different name, it is better to define an array and
 * store all the elements into it.
 * 
 * 
 */